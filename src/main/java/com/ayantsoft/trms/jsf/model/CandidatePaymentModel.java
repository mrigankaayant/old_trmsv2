package com.ayantsoft.trms.jsf.model;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.ArrayList;
import java.util.List;

import com.ayantsoft.trms.hibernate.pojo.Candidate;
import com.ayantsoft.trms.hibernate.pojo.CandidatePayment;


public class CandidatePaymentModel implements Serializable{

	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 2773781986036402254L;

	private Candidate candidate;

	private String candidateEmail;
	private String candidateMobile;
	private String course;
	private BigDecimal totalAmount;
	private BigDecimal paidAmount;
	private BigDecimal dueAmount;
	private String recruiterName;
	private Integer recruiterEmpId;
	private String recruiterEmail;
	private String recruiterMobile;
	private List<CandidatePayment> candidatePayments = new ArrayList<CandidatePayment>();
	
	public Candidate getCandidate() {
		return candidate;
	}
	public void setCandidate(Candidate candidate) {
		this.candidate = candidate;
	}
	public String getCandidateEmail() {
		return candidateEmail;
	}
	public void setCandidateEmail(String candidateEmail) {
		this.candidateEmail = candidateEmail;
	}
	public String getCandidateMobile() {
		return candidateMobile;
	}
	public void setCandidateMobile(String candidateMobile) {
		this.candidateMobile = candidateMobile;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public BigDecimal getTotalAmount() {
		return totalAmount;
	}
	public void setTotalAmount(BigDecimal totalAmount) {
		this.totalAmount = totalAmount;
	}
	public BigDecimal getPaidAmount() {
		return paidAmount;
	}
	public void setPaidAmount(BigDecimal paidAmount) {
		this.paidAmount = paidAmount;
	}
	public BigDecimal getDueAmount() {
		return dueAmount;
	}
	public void setDueAmount(BigDecimal dueAmount) {
		this.dueAmount = dueAmount;
	}
	
	public List<CandidatePayment> getCandidatePayments() {
		return candidatePayments;
	}
	public void setCandidatePayments(List<CandidatePayment> candidatePayments) {
		this.candidatePayments = candidatePayments;
	}
	public String getRecruiterName() {
		return recruiterName;
	}
	public void setRecruiterName(String recruiterName) {
		this.recruiterName = recruiterName;
	}
	public Integer getRecruiterEmpId() {
		return recruiterEmpId;
	}
	public void setRecruiterEmpId(Integer recruiterEmpId) {
		this.recruiterEmpId = recruiterEmpId;
	}
	public String getRecruiterEmail() {
		return recruiterEmail;
	}
	public void setRecruiterEmail(String recruiterEmail) {
		this.recruiterEmail = recruiterEmail;
	}
	public String getRecruiterMobile() {
		return recruiterMobile;
	}
	public void setRecruiterMobile(String recruiterMobile) {
		this.recruiterMobile = recruiterMobile;
	}
	
}